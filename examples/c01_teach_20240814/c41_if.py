weekday = 3  # 一周的第三天，星期三

if weekday == 1:
    print("星期一")
elif weekday == 2:
    print("星期二")
elif weekday == 3:
    print("星期三")
elif weekday == 4:
    print("星期四")
elif weekday == 5:
    print("星期五")
elif weekday == 6:
    print("星期六")
elif weekday == 7:
    print("星期天")
else:
    print("错误的数字")
